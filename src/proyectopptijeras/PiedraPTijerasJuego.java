    private void jComboBoxColorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxColorActionPerformed
        int color = jComboBoxColor.getSelectedIndex();
        juego.setColor(color);
        switch(juego.getColor()){
            case 0: 
                jTextArea3.setForeground(Color.red);
                break;
            case 1:
                jTextArea3.setForeground(Color.yellow);
                break;
            case 2:
                jTextArea3.setForeground(Color.green);
                break;
            case 3:
                jTextArea3.setForeground(Color.blue);
        }
        jComboBoxColor.setEnabled(false);
    }//GEN-LAST:event_jComboBoxColorActionPerformed

